#include <stdio.h>
/**
*print_to_98 - prints from number to 98
*@n: is the number to be printed from
*Return: sum
 */

void print_to_98(int n)
{
	if (n >= 98)
	{
		while (n > 98)
			printf("%d, ", n--);
		printf("%d\n", n);
	}

	else
	{
		while (n < 98)
			printf("%d, ", n++);
		printf("%d\n", n);
	}
}
